# How can you assign the following rhyme to a single variable
# while preserving the line break?
string = "A pirate I was meant to be! \nTrim the sails and roam the sea!"
print(string)
# A pirate I was meant to be!
# Trim the sails and roam the sea!

# OR wrap in triple quotes
string2 = """A pirate I was meant to be!
Trim the sails and roam the sea!"""
print(string2)
# A pirate I was meant to be!
# Trim the sails and roam the sea!
